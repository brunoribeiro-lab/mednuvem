    public static function extractID($youtubeLink) {
        $videoID = NULL;
        if (preg_match('/youtube\.com/', $youtubeLink)) {
            $parse = parse_url($youtubeLink, PHP_URL_QUERY);
            parse_str($parse, $output);
            if (empty($output['v']))
                return NULL;

            $videoID = $output['v'];
        }
        if (preg_match('/youtu\.be/', $youtubeLink)) {
            $url = parse_url($youtubeLink, PHP_URL_PATH);
            $videoID = substr($url, 1, 11);
        }
        return $videoID;
    }

